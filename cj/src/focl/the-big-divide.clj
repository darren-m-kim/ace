(defn f1 [n d1 d2]
  (let [divisible? (fn [n d]
                     (= 0 (rem n d)))]
    (->> n
         range      
         (filter #(or (divisible? % d1)
                      (divisible? % d2)))
         (apply +))))
;; => #'user/f1
(= 0 (f1 3 17 11))
;; => true
(= 23 (f1 10 3 5))
;; => true
(= 233168 (f1 1000 3 5))
;; => true
(= "2333333316666668" (str (f1 100000000 3 5)))
;; => true
(= "110389610389889610389610"
   (str (f1 (* 10000 10000 10000) 7 11)))
(= "1277732511922987429116"
   (str (f1 (* 10000 10000 10000) 757 809)))
(= "4530161696788274281"
   (str (f1 (* 10000 10000 1000) 1597 3571)))
